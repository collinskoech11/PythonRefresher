def initials(Fname, Lname):
    #initialize the string
    Fname = "Noel"
    Lname = "Wafuko"

    #extract first character by index
    first_name_char = Fname[0]
    last_name_char = Lname[0]

    #capitalize letters
    first_name_char.capitalize()
    last_name_char.capitalize()

    #return initials 
    return first_name_char()
    return last_name_char()
